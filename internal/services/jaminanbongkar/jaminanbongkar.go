const source = "jaminanbongkar"

func Create(input m.CreateDto, userId uint, tx *gorm.DB) (any, error) {
	if tx == nil {
		tx = a.DB
	}
	var data m.JaminanBongkar

	// copy input (payload) ke struct data satu if karene error dipakai sekali, +error
	if err := sc.Copy(&data, &input); err != nil {
		return sh.SetError("request", "create-data", source, "failed", "gagal mengambil data payload", data)
	}

	var dataSpt spt.Spt
	result := a.DB.First(&dataSpt, "\"Id\" = ?", data.Spt_Id.String())
	if result.RowsAffected == 0 {
		return nil, nil
	} else if result.Error != nil {
		return sh.SetError("request", "create", source, "failed", "gagal mengambil data spt", data)
	}
	noUrut := regexp.MustCompile(`(\d{5})$`).FindString(dataSpt.NomorSpt)
	data.Nomor = fmt.Sprintf("JB-0000%s", noUrut)

	// calc

	// simpan data ke db satu if karena result dipakai sekali, +error
	if result := tx.Create(&data); result.Error != nil {
		return sh.SetError("request", "create-data", source, "failed", "gagal mengambil menyimpan data", data)
	}

	return rp.OKSimple{Data: data}, nil
}

